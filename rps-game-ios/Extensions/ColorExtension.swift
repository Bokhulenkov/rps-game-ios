import UIKit

extension UIColor {
    struct CustomColors {
        static let darkBlueTimeline = UIColor(named: "darkBlueTimelineColor") ?? UIColor()
        static let darkGreenTimeline = UIColor(named: "darkGreenTimelineColor") ?? UIColor()
        static let darkPurpleBlueText = UIColor(named: "darkPurpleBlueTextColor") ?? UIColor()
        static let darkTangerineText = UIColor(named: "darkTangerineTextColor") ?? UIColor()
        static let orangeBackgroundButton = UIColor(named: "orangeBackButtonColor") ?? UIColor()
        static let orangePrimaryButton = UIColor(named: "orangePrimaryButtonColor") ?? UIColor()
        static let orangeShadowButton = UIColor(named: "orangeShadowButtonColor") ?? UIColor()
        static let pastelYellowText = UIColor(named: "pastelYellowTextColor") ?? UIColor()
        static let darkYellowPinkText = UIColor(named: "darkYellowPink") ?? UIColor()
        static let darkBrown = UIColor(named: "dark-brown") ?? UIColor()
        static let customBlack = UIColor(named: "customBlack") ?? UIColor()
		static let darkBlueCircle = UIColor(named: "darkBlueCircle") ?? UIColor()
    }
}
